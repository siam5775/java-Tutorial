public class day {
    public static void main(String[] args) {
       int day = 1;
       
       switch(day){
        case 1:
            System.out.println("Monday");
            break;
        case 2:
            System.out.println("tuesday");
            break;
        default:
            System.out.println("wed-sun");

       }
    }
}
